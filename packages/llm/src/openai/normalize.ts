import {
  REPORT_SCHEMA_VERSION,
  type AudienceAccount,
  type AudienceDistribution,
  type ScoreMetric,
  type ScoreValue,
} from "@kol-fit/shared";

import type { GenerateFitReportInput } from "../provider.js";

type Raw = Record<string, unknown>;

/** Recursively convert JSON `null` (strict-mode optionals) to `undefined`. */
export function deepNullToUndefined(value: unknown): unknown {
  if (value === null) return undefined;
  if (Array.isArray(value)) return value.map(deepNullToUndefined);
  if (value && typeof value === "object") {
    const out: Raw = {};
    for (const [k, v] of Object.entries(value as Raw)) out[k] = deepNullToUndefined(v);
    return out;
  }
  return value;
}

/**
 * Deterministic distribution over classified accounts, on all three axes
 * (Unit 43). Every axis shares the SAME denominator — the full classified
 * sample — so the three views are directly comparable and none of them quietly
 * counts a different set of people.
 */
export function buildAudienceDistribution(
  accounts: AudienceAccount[]
): AudienceDistribution {
  const sampleSize = accounts.length;
  const tally = <K extends string>(pick: (a: AudienceAccount) => K) => {
    const counts = new Map<K, number>();
    for (const a of accounts) {
      const k = pick(a);
      counts.set(k, (counts.get(k) ?? 0) + 1);
    }
    const out: Partial<Record<K, { count: number; share: number }>> = {};
    for (const [k, count] of counts) {
      out[k] = { count, share: sampleSize === 0 ? 0 : count / sampleSize };
    }
    return out;
  };
  return {
    sampleSize,
    roles: tally((a) => a.role),
    domains: tally((a) => a.domain),
    quality: tally((a) => a.quality),
  };
}

/**
 * Assemble a FitReport from the model's narrative + the deterministic inputs.
 * Every numeric/score/verdict field comes from `input` — never the model.
 */
export function assembleFitReport(
  narrative: Raw,
  input: GenerateFitReportInput
): unknown {
  const placeholder = (metric: string): ScoreValue => ({
    value: 0,
    confidence: "low",
    reasons: [`No deterministic score available for ${metric}.`],
  });
  const component = (metric: ScoreMetric): ScoreValue =>
    input.scores?.components?.[metric] ?? placeholder(metric);

  const str = (v: unknown): string => (typeof v === "string" ? v : "");
  const arr = (v: unknown): string[] =>
    Array.isArray(v) ? v.filter((x): x is string => typeof x === "string") : [];

  return {
    schemaVersion: REPORT_SCHEMA_VERSION,
    overallScore: input.scores?.overall ?? placeholder("overall_fit"),
    verdict: input.verdict ?? "OKAY",
    confidence: input.scores?.confidence ?? "low",
    summary: str(narrative.summary) || undefined,
    keyTakeaways: arr(narrative.keyTakeaways),
    evidence: {
      sampleSizes:
        input.sampleSizes ?? { engagedAccounts: input.audience.distribution.sampleSize },
      notes: [
        "Narrative generated by the OpenAI provider; numeric scores are deterministic (packages/scoring).",
        ...arr(narrative.evidenceNotes),
      ],
    },
    bestUseCases: arr(narrative.bestUseCases),
    weakUseCases: arr(narrative.weakUseCases),
    audienceMatch: {
      summary: str(narrative.audienceMatchSummary),
      score: component("engaged_audience_match"),
    },
    audienceBreakdown: input.audience.distribution,
    contentAnalysis: {
      classification: input.kol.content,
      narrative: str(narrative.contentNarrative),
    },
    engagementQuality: {
      narrative: str(narrative.engagementNarrative),
      signals: arr(narrative.engagementSignals),
    },
    paidPromo: {
      narrative: str(narrative.paidPromoNarrative),
      riskScore: component("paid_promo_risk"),
    },
    botFarmRisk: {
      narrative: str(narrative.botFarmNarrative),
      riskScore: component("bot_farm_risk"),
    },
    brandSafety: {
      narrative: str(narrative.brandSafetyNarrative),
      score: component("brand_safety"),
    },
    geoLanguageFit: {
      narrative: str(narrative.geoNarrative),
      score: component("geo_language_fit"),
    },
    recommendedAngle: str(narrative.recommendedAngle),
  };
}
